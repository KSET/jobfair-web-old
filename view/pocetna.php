  <h1>JobFair 2011. - review</h1>
  <p>
    Nakon završetka još jednog u nizu JobFaira, statističke informacije
ovogodišnjeg događaja su u nastavku.
  </p>
  <p>
JobFair 2011. okupio je <b>23 sudionika</b> raspodijeljenih u tri područja:
  <ul>
    <li><b>visokotehnološke tvrtke</b> koju su prestavile svoj rad i mogućnost zaposlenja,</li>
    <li><b>obrazovne institucije</b> koje su predstavile mogućnost daljnjeg znanstvenog rada, </li>
    <li><b>mlade poduzetnike</b> koje su omogućile uvid u razvoj vlastitog biznisa.</li>
  </ul>
  </p>
  <p>
Organizirali smo <b>15 predavanja</b> na koja se možete podsjetiti <a href="/media/">ovdje</a>, aula Fakulteta elektrotehnike i računarstva bila je dva dana popunjena sa <b>12 info štandova</b> te smo sakupili preko <b>300 životopisa</b> studenata. 
  </p>
  <p>
Cijeli događaj je medijski popraćen na čemu zahvaljujemo <a href="http://www.rep.hr">REP.hr</a>-u, <a href="http://www.radiostudent.fpzg.hr">Radio
Studentu</a>, <a href="http://www.e-hrvatska.hr">e-Hrvatskoj</a>, itd., a podržale su nas velike institucije poput
<a href="http://www.hgk.hr">Hrvatske gospodarske komore</a> i <a href="http://www.hup.hr/">Hrvatske udruge poslodavaca</a>. Iznimno
zahvaljujemo <a href="http://www.fer.hr">Fakulteta elektrotehnike i računarstva</a> koji nam je omogućio prostor, podršku i sudioništvo.
  </p>
  <p>
Zainteresirane buduće sudonike pozivamo da nam se obrate putem <a href="mailto:jobfair@kset.org">maila</a> kako bi dobili sve relevantne informacije za sudioništvo sljedeće godine :)
  </p>
  <p>
Još jednom zahvaljujemo svima na podršci i sudjelovanju te se nadamo vidjeti Vas i do godine :)
  </p>

