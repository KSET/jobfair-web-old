  <h1>Zaposlenje</h1>
  <p>
    <strong>Još kao student se informirajte o vještinama i znanjima koje tvrtke traže, ali i o pogodnostima koje nude.</strong>
  </p>
  <p>
    Spoznajte svoje prednosti i mane, upoznajte se s aktualnim tehnologijama, ali i onima koje tek dolaze, a ako imate viška vremena počnite već za vrijeme studija razmišljati o honorarnom poslu.
  </p>
  <p>
    JobFair je prilika da iz prve ruke saznate kako će izgledati vaš sutrašnje radno mjesto i da se što bolje pripremite za njega.
  </p>
