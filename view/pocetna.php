  <p>
    <strong>
      Nakon godine izbivanja odlučili smo ponovno organizirati <b>JobFair</b>, ovog puta proširenog koncepta koji pruža studentima potpuni uvid u opcije nakon diplome. Uz uobičajeno predstavljanje tvrtki, ove godine će studenti imati priliku dodatno se informirati o mogućnostima daljnjeg obrazovanja i znanstvenog rada te o pokretanju vlastite tvrtke.
    </strong>
  </p>
  <p>
    Ukoliko ste tvrtka u potrazi za izvrsnim studentima, visokoškolovanim magistrima ili mogućim partnerima, JobFair je idealna prilika za promociju Vaše tvrtke u prostorima FER-a te ostvarivanje direktnih kontakata sa budućim zaposlenicima ili suradnicima.
  </p>
  <p>
    Kao studenti se imate priliku već za vrijeme studija informirati o mogućnostima nakon stjecanja diplome, te nam je cilj prezentirati tri najčešća smjera u koje možete krenuti:
    <ul>
      <li>zaposlenje u nekoj od viskotehnoloških tvrtki,</li>
      <li>nastavak visokog obrazovanja i izgradnja znanstvene karijere,</li>
      <li>pokretanje vlastite tvrtke.</li>
    </ul>
  </p>
  <p>
    Područje koje JobFair pokriva je tradicionalno ICT, ali i druge visokotehnološke inženjerske grane, poput strojarstva, elektrotehnike i energetike.
  </p>
  <p>
    Ove godine kvalitetu i važnost JobFaira prepoznala je <a href="http://www.hgk.hr">Hrvatska gospodarska komora</a> i <a href="http://www.hup.hr">Hrvatska udruga poslodavaca</a>.
  </p>
  <p>
    Proširite svoje znanje i pridružite nam se <b>18. i 19. svibnja</b> u prostorima Fakulteta elektrotehnike i računarstva.
  </p>

