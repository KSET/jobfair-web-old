  <h1>KSET</h1>

  <p>
    <b>Klub studenata elektrotehnike</b> je kroz duge 34 godine postojanja stekao već kultni status u Zagrebu, ali i šire. Osnova Kluba je ostala nepromijenjena do danas, a to je studentski volonterizam; danas klub broji preko 200 aktivnih članova organiziranih u 9 sekcija te preko 300 počasnih članova.
  </p>
  <p>
    Osim večernjeg kulturnog programa (koncerti, slušaonice, predstave, filmske projekcije, ...) članovi Kluba tradicionalno organiziraju Brucošijadu FER-a, PowerTrip, JobFair, vode nekoliko vještina na fakultetu, snimaju kratke filmove, organiziraju (biciklističke) izlete, održavaju stručne tečajeve, programiraju, projektiraju, popravljaju, ... i na taj način dolaze do nezamjenjivog praktičnog iskustva koje fakultet nije uvijek u mogućnosti pružiti.
