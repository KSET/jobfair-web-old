  <h1>SSFER</h1>
  
  <p>
    <b>Savez studenata Fakulteta elektrotehnike i računarstva</b> je udruga koja okuplja mlade, inovatine i entuzijastične studente FER-a pod svojim okriljem s ciljem poboljšanja kvalitete studenskog života i pružanja različitih vannastavnih aktivnosti kako bi se bolje primjenilo i shvatilo znanje stečeno na Fakultetu.
  </p>
