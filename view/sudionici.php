      <br /><br />
      Kao i većina zračnih brodova, zračni brod Hipersfera je ispunjen plinom koji je lakši od zraka, tako da je njegova prosječna gustoća jednaka gustoći okruženja – što stvara sile uzgona. Omotač zračnog broda i potporna struktura su dovoljno jaki kako bi održali brod i korisni teret te kako bi izdržali male razlike u tlaku, a dovoljno su lagani kako bi omogućili letjelici da bude lakša od zraka. Potporna struktura napravljena je od kompozita ojačanih ugljičnim vlaknima. 
      <br /><br />
      Usješno je izveden prvi primjerak od tri modela u skali, u omjeru 1:11,5 (D=3,6 m). Namjena prvog modela zračnog broda bila je testirati strukturu i osnovne postavke aerodinamike te automatike upravljanja zračnim brodom. Maksimalni visinski domet broda je 100 m, ukupno je ostvareno više od 500 sati leta, a brod je prošao je sve ciljne testove i služi kao pokazni model u prikupljanju investicije za iduću financijsku fazu razvojnog projekta, kao i za konceptualnu evaluaciju podsustava za veće modele.
      <br /><br />
